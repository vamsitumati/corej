package switchscenarios;

public class SwitchSimple {
	public static void main(String args[]){
	int num= 10;
	
	switch(num)
	{
	case 40:
		System.out.println("Found");
		break;
	case 20:
		System.out.println("Found");
		break;
	case 30:
		System.out.println("Found");
		break;
	default:
		System.out.println("Not Found");
	}
	

}
}
